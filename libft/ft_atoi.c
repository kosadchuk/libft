#include "libft.h"

int		ft_atoi(const char *str)
{
	size_t		i;
	long int	j;
	int			x;

	i = 0;
	j = 0;
	x = 1;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
		x = -1;
	if (str[i] == '-' || str[i] == '+')
		i++;
	while (str[i] != '\0' && (str[i] >= '0' && str[i] <= '9'))
	{
		if ((j > 922337203685477580 || (j == 922337203685477580
			&& (str[i] - '0') > 7)) && x == 1)
			return (-1);
		else if ((j > 922337203685477580 || (j == 922337203685477580
			&& (str[i] - '0') > 8)) && x == -1)
			return (0);
		j = (j * 10) + (str[i] - '0');
		i++;
	}
	return (j * x);
}
